#include "ft_printf.h"

int	ft_let(char c)
{
	if (c == 'c' || c == 's' || c == 'p' ||
		c == 'd' || c == 'i' || c == 'u' ||
		c == 'x' || c == 'X' || c == '%')
		return (1);
	else
		return (0);
}

int	ft_digit(char c)
{
	if (c >= '0' && c <= '9')
		return (1);
	else
		return (0);
}

int	ft_flag(char c)
{
	if (c == '.' || c == '*' || c == '-' || c == '0')
		return (1);
	else
		return (0);
}
